void solve()
{
    int n, s;
    cin >> n >> s;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    int ans = 0;
    if(s <= a[0])
        ans = a[n-1] - s;
    else if(s >= a[n-1])
        ans = s - a[0];
    else
    {
        int left = s - a[0];
        int right = a[n-1] - s;
        ans = (a[n-1] - a[0]) + min(left,right);
    }
    cout << ans << "\n";
    
}

