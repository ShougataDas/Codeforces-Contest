void solve()
{
    int n;
    cin >> n;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
    }

    int mn = LLONG_MAX;
    for (int i = 1; i <= n; i++)
    {
        int j = i + 1;
        while (j <= n && a[j] == a[i])
            j++;
        int tt = ((i - 1) * a[i]) + ((n - j + 1) * a[i]);
        mn = min(mn, tt);
        i = j-1;
    }
    cout << mn << "\n";
}

